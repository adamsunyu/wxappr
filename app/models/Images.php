namespace Phosphorum\Models;

use Phalcon\Mvc\Model;

/**
 * Phosphorum\Models\Images
 */
class Images extends Model
{

    /**
     * ID
     * @var integer
     */
    public $id;

    /**
     * UsersId
     * @var string
     */
    public $users_id;

    /**
     * Define relationships to Users
     */
    public function initialize()
    {
        $this->belongsTo(
            'users_id',
            'Phosphorum\Models\Users',
            'id',
            [
                'alias'    => 'user',
                'reusable' => true
            ]
        );
    }
}
